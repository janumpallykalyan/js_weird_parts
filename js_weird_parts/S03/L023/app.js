var a = 1 + '2';
console.log(a);

/*

The result of the above is 12. This is because Javascript coerced the number 1 into the string '1'
and concatenated the two strings together to make '12'.

Coercion is when Javascript attemps to 'coerce' or convert a value of one type to another.

It happens quite often as JavaScript is a dynamically typed language.

*/